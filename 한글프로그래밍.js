// 세종대왕이 만든 한글 프로그래밍 언어 예제

// 한글 키워드 정의
const 만약 = (조건, 참일때, 거짓일때) => 조건 ? 참일때() : 거짓일때?.();
const 반복 = (횟수, 실행함수) => {
    for (let i = 0; i < 횟수; i++) {
        실행함수(i);
    }
};
const 출력 = console.log;
const 입력 = prompt;

// 변수 선언을 한글로
const 숫자 = (값) => Number(값);
const 문자 = (값) => String(값);
const 목록 = (값) => Array.isArray(값) ? 값 : [];

// 게시판 데이터 구조
let 게시판목록 = [];
let 글번호 = 1;

// 글 작성 함수
function 글쓰기(제목, 내용, 작성자) {
    const 새글 = {
        번호: 글번호++,
        제목: 제목,
        내용: 내용,
        작성자: 작성자,
        작성일: new Date().toLocaleDateString('ko-KR'),
        조회수: 0
    };

    게시판목록.push(새글);
    출력(`새 글이 작성되었습니다: ${새글.제목}`);
    return 새글;
}

// 글 목록 보기
function 목록보기() {
    출력('\n=== 세종대왕의 게시판 ===');
    출력('번호\t제목\t\t작성자\t작성일\t조회수');
    출력('─'.repeat(50));

    게시판목록.forEach(글 => {
        출력(`${글.번호}\t${글.제목}\t${글.작성자}\t${글.작성일}\t${글.조회수}`);
    });
}

// 글 읽기
function 글읽기(번호) {
    const 글 = 게시판목록.find(글 => 글.번호 === 번호);

    만약(
        글,
        () => {
            글.조회수++;
            출력(`\n제목: ${글.제목}`);
            출력(`작성자: ${글.작성자}`);
            출력(`작성일: ${글.작성일}`);
            출력(`조회수: ${글.조회수}`);
            출력(`\n내용:\n${글.내용}`);
        },
        () => 출력('해당 글을 찾을 수 없습니다.')
    );
}

// 예제 실행
출력('세종대왕의 한글 프로그래밍 언어로 만든 게시판');

// 샘플 데이터 생성
글쓰기('한글창제에 대하여', '한글은 우리나라의 자랑스러운 문자입니다.', '세종대왕');
글쓰기('백성들을 위한 글자', '누구나 쉽게 배울 수 있는 글자를 만들었습니다.', '집현전학사');
글쓰기('훈민정음 해례본', '한글의 제자원리를 설명한 책입니다.', '정인지');

// 게시판 기능 테스트
목록보기();
출력('\n첫 번째 글 읽기:');
글읽기(1);

// 한글 반복문 예제
출력('\n한글 반복문 예제:');
반복(3, (번째) => {
    출력(`${번째 + 1}번째 반복입니다.`);
});

